package model;

/**
 *
 * @author LEGION 5
 */
public class Cinema {
    private String id;
    private String name;
    private int numOfRoom;
    private String cityId;

    public Cinema() {
    }

    public Cinema(String id, String name, int numOfRoom, String cityId) {
        this.id = id;
        this.name = name;
        this.numOfRoom = numOfRoom;
        this.cityId = cityId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumOfRoom() {
        return numOfRoom;
    }

    public void setNumOfRoom(int numOfRoom) {
        this.numOfRoom = numOfRoom;
    }

    public String getCityId() {
        return cityId;
    }

    public void setCityId(String cityId) {
        this.cityId = cityId;
    }
    
}
