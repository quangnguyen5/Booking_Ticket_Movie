package model;

/**
 *
 * @author LEGION 5
 */
public class City {
    private String id;
    private String name;
    private String state;
    private String zipcode;

    public City() {
    }

    public City(String id, String name, String state, String zipcode) {
        this.id = id;
        this.name = name;
        this.state = state;
        this.zipcode = zipcode;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZipcode() {
        return zipcode;
    }

    public void setZipcode(String zipcode) {
        this.zipcode = zipcode;
    }
    
}
